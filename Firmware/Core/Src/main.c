void getADCData()
{
	sConfig.Channel = ADC_CHANNEL_0;
	HAL_ADC_ConfigChannel(&hadc1, &sConfig);
    HAL_ADC_Start(&hadc1);
    HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
    therm_val = HAL_ADC_GetValue(&hadc1);
	therm_val_bytes[0] = (uint8_t)(therm_val >> 8);
	therm_val_bytes[1] = (uint8_t)therm_val;
	fThermTemp = (float)therm_val*3.3/4095.0;
	fThermTemp = fThermTemp * thermSlope + thermOffset;
}


void sweepBuckDuty()
{
	if(direction == 1)
		{
			if(duty <= 15988)
			{
				duty+=2000;
				TIM1->CCR1 = duty;
			}
			if(duty >= 100)
			{
				direction = 0;
			}
		}

		if(direction == 0)
		{
			if(duty >= 0)
			{
				duty-=2000;
				TIM1->CCR1 = duty;
			}
			if(duty <= 0)
			{
				direction = 1;
			}
		}
}
